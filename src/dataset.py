"""
dataset.py
----------
PyTorch Dataset for loading ERSP spectrograms from BCI-IV-2b.
Compatible with DataLoader for mini-batch training.

Usage:
    from dataset import ERSPDataset, build_loaders
    train_loader, val_loader = build_loaders(subjects=[1,2,3], suffix='T')
"""

import sys
from pathlib import Path
import numpy as np
import torch
from torch.utils.data import Dataset, DataLoader, random_split

sys.path.insert(0, str(Path(__file__).parent))
from config import (
    DATA_PROC, SUBJECTS, BATCH_SIZE, VAL_SPLIT, RANDOM_SEED,
    N_CHANNELS, IMG_FREQ_BINS, IMG_TIME_BINS, TRAIN_SUFFIX, EVAL_SUFFIX
)


class ERSPDataset(Dataset):
    """
    ERSP spectrogram dataset for BCI-IV-2b.

    Loads .npz files generated by ersp.py and serves them
    as (X, y) tensors for PyTorch.

    Parameters
    ----------
    subjects : list of ints (1–9) with subjects to include
    suffix   : 'T' (training / sessions 1-3) or 'E' (evaluation / sessions 4-5)
    transform: optional transform applied to images (data augmentation)
    """

    def __init__(self,
                 subjects: list = None,
                 suffix: str = TRAIN_SUFFIX,
                 transform=None):
        if subjects is None:
            subjects = SUBJECTS

        self.transform = transform
        X_all, y_all = [], []

        for subj in subjects:
            tag = f"S{subj:02d}{suffix}"
            npz_path = DATA_PROC / f"{tag}-ersp.npz"

            if not npz_path.exists():
                print(f"  Warning: {npz_path.name} not found — subject skipped.")
                continue

            data = np.load(str(npz_path))
            X_all.append(data["X"])   # (N, C, F, T)
            y_all.append(data["y"])   # (N,)

        if not X_all:
            raise FileNotFoundError(
                "No ERSP files found. "
                "Run first: python src/ersp.py"
            )

        self.X = torch.tensor(np.concatenate(X_all, axis=0), dtype=torch.float32)
        self.y = torch.tensor(np.concatenate(y_all, axis=0), dtype=torch.long)

        n_left  = (self.y == 0).sum().item()
        n_right = (self.y == 1).sum().item()
        print(f"  Dataset loaded: {len(self.X)} images "
              f"(left: {n_left}, right: {n_right}) — "
              f"shape: {tuple(self.X.shape)}")

    def __len__(self) -> int:
        return len(self.X)

    def __getitem__(self, idx: int):
        x = self.X[idx]
        y = self.y[idx]
        if self.transform is not None:
            x = self.transform(x)
        return x, y

    @property
    def n_classes(self) -> int:
        return int(self.y.max().item()) + 1

    @property
    def class_weights(self) -> torch.Tensor:
        """Inverse-frequency class weights (useful for imbalanced datasets)."""
        counts = torch.bincount(self.y)
        weights = 1.0 / counts.float()
        weights = weights / weights.sum()
        return weights


def build_loaders(subjects: list = None,
                  suffix_train: str = TRAIN_SUFFIX,
                  suffix_test: str = EVAL_SUFFIX,
                  batch_size: int = BATCH_SIZE,
                  val_split: float = VAL_SPLIT,
                  seed: int = RANDOM_SEED):
    """
    Build training, validation, and test DataLoaders.

    BCI-IV-2b protocol:
        - Training: sessions 1-3 (suffix='T'), with val_split reserved for validation
        - Test: sessions 4-5 (suffix='E')

    Parameters
    ----------
    subjects     : list of subjects (None = all)
    suffix_train : 'T' for training sessions
    suffix_test  : 'E' for evaluation sessions
    batch_size   : mini-batch size
    val_split    : fraction of training set used for validation
    seed         : random seed for reproducibility

    Returns
    -------
    train_loader, val_loader, test_loader
    """
    if subjects is None:
        subjects = SUBJECTS

    print("\n── Loading training dataset ──")
    train_full = ERSPDataset(subjects=subjects, suffix=suffix_train)

    print("\n── Loading evaluation dataset ──")
    test_ds = ERSPDataset(subjects=subjects, suffix=suffix_test)

    # Train / validation split
    n_val   = int(len(train_full) * val_split)
    n_train = len(train_full) - n_val
    generator = torch.Generator().manual_seed(seed)
    train_ds, val_ds = random_split(
        train_full, [n_train, n_val], generator=generator
    )

    train_loader = DataLoader(train_ds, batch_size=batch_size,
                              shuffle=True,  num_workers=0, pin_memory=False)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size,
                              shuffle=False, num_workers=0, pin_memory=False)
    test_loader  = DataLoader(test_ds,  batch_size=batch_size,
                              shuffle=False, num_workers=0, pin_memory=False)

    print(f"\n  Sizes: train={n_train}, val={n_val}, test={len(test_ds)}")
    return train_loader, val_loader, test_loader


